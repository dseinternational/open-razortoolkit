namespace DSE.Open.RazorToolkit.UI.Web.Components.Html.Controls;

/// <summary>
/// Describes alignment for a <see cref="HtmlDataGrid{TGridItem}"/> column.
/// </summary>
public enum Align
{
    /// <summary>
    /// Justifies the content against the start of the container.
    /// </summary>
    Left,

    /// <summary>
    /// Justifies the content at the center of the container.
    /// </summary>
    Center,

    /// <summary>
    /// Justifies the content at the end of the container.
    /// </summary>
    Right,
}
